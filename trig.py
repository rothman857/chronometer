import math

def acos(x: float):
    return math.degrees(math.acos(x))


def asin(x: float):
    return math.degrees(math.asin(x))


def atan(x: float):
    return math.degrees(math.atan(x))


def sin(deg: float):
    return math.sin(math.radians(deg))


def cos(deg: float):
    return math.cos(math.radians(deg))


def tan(deg: float):
    return math.tan(math.radians(deg))


